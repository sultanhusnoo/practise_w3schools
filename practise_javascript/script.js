function darkMode(){
    document.getElementById('myBody').style.backgroundColor = "#181818";
    document.getElementsByClassName('bodyText').style.color = 'white';
}

function lightMode(){
    document.getElementById('myBody').style.backgroundColor = 'white';
    document.getElementById('bodyText').style.color = 'black';
}                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                        